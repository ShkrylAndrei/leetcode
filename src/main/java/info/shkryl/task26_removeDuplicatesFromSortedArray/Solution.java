package info.shkryl.task26_removeDuplicatesFromSortedArray;

public class Solution {
    public int removeDuplicates(int[] nums) {
        // Если массив пустой (хотя по условию длина >= 1, но для надёжности)
        if (nums.length == 0) {
            return 0;
        }

        // Указатель на позицию для следующего уникального элемента
        // Первый элемент всегда уникален, поэтому начинаем с индекса 1
        int i = 1;

        // Проходим по массиву, начиная со второго элемента
        for (int j = 1; j < nums.length; j++) {
            // Если текущий элемент отличается от предыдущего — он уникален!
            if (nums[j] != nums[j - 1]) {
                nums[i] = nums[j]; // Записываем его в "уникальную зону"
                i++;               // Двигаем границу уникальных элементов
            }
        }

        // i — это количество уникальных элементов
        return i;
    }
}
